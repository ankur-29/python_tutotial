# primitive data type :---> int, float, bool, str, none

# composite data type :---> list, tuple, set, dict

# Integers
age = 25
type(age)  # <class 'int'> // represents an integer

# Float
height = 5.9
type(height)  # <class 'float'> // represents a floating-point number

# Boolean --> True and False
is_student = True
type(is_student)  # <class 'bool'> // represents a boolean value

# String
name = "John"
type(name)  # <class 'str'> // represents a string


# None
value = None
type(value)  # <class 'NoneType'> // represents a null value